# This file contains examples of different data types in Python.

# Integer
age = 30
print("Age:", age)

# Float
height = 5.9
print("Height:", height)

# String
name = "Alice"
print("Name:", name)

# Boolean
is_student = True
print("Is Student:", is_student)

# List
fruits = ["apple", "banana", "cherry"]
print("Fruits:", fruits)

# Tuple
coordinates = (10.0, 20.0)
print("Coordinates:", coordinates)

# Set
unique_numbers = {1, 2, 3, 4, 5}
print("Unique Numbers:", unique_numbers)

# Dictionary
person = {
    "name": "Bob",
    "age": 25,
    "city": "New York"
}
print("Person:", person)